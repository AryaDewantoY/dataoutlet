<h2>CK Distro</h2><br>
<p>Toko busana CK Distro merupakan usaha dagang yang bergerak dalam penjualan pakaian. Toko busana ini berdiri pada tahun 2015 yang didirikan oleh Bapak Amrizal. Toko busana ini merupakan usaha keluarga. 
         Toko busana ini digerakkan oleh 2 orang yaitu Bapak Amrizal dan Ibu Ismawati.


</p><br>
Adapun visi dan misi yang ingin dicapai oleh Toko Busana CK Distro adalah:<br><br>
1. Visi<br>
a.	Memberikan yang terbaik dalam pelayanan dan kenyamanan pelanggan.<br>
b.	Memberikan jasa pelayanan barang yang terbaik bagi pelanggan.<br>
c.	Mengutamakan kepuasan pelanggan.<br><br>
      2. Misi<br>
a.	Menyediakan sarana dan prasarana yang berkualitas. <br>
b.	Memberikan pelayanan yang cepat, akurat dan terpercaya dengan biaya terjangkau.



